export function t(
  locale: Locale,
  key: string,
  vars?: Record<string, string | number>
): string {
  let s = messages[locale][key] ?? messages.en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replaceAll(`{${k}}`, String(v));
    }
  }
  return s;
}
